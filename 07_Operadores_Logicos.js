// AND  &&

console.log("Operador Logico AND");

var a = 8;
console.log((a>5)&&(a<10));
console.log((a>10)&&(a<10));

// OR ||
console.log("OR");
console.log((a>10)||(a==3)); //falso
console.log((a>10)||(a==8)); //True

// NOT

console.log("NOT");
console.log(!(a>5)); //Es True pero se va a negar
console.log(a>5);



